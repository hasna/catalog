function layout(title: string, body: string, options: GenerateSiteOptions): string {
  const generatedAt = options.generatedAt ?? new Date().toISOString();
  return `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${escapeHtml(title)}</title>
<style>${STYLES}</style>
</head>
<body>
${body}
<footer>Generated by @hasna/catalog on ${escapeHtml(generatedAt)}.</footer>
</body>
</html>
`;
}

export function renderIndexPage(options: GenerateSiteOptions): string {
  const siteName = options.siteName ?? "Hasna App Catalog";
  const cards = options.apps
    .map((app) => {
      const version = appVersion(app);
      return `<li class="card">
<h2><a href="apps/${escapeHtml(app.appId)}/">${escapeHtml(app.appId)}</a></h2>
<p>${escapeHtml(app.summary ?? app.npmName)}</p>
<div class="badges">
${version ? `<span class="badge version">v${escapeHtml(version)}</span>` : ""}
<span class="badge">${escapeHtml(app.lifecycle)}</span>
<span class="badge">${escapeHtml(app.releaseChannel)}</span>
</div>
</li>`;
    })
    .join("\n");
  const body = `<header class="site">
<h1>${escapeHtml(siteName)}</h1>
<p>${options.apps.length} open-source apps, every one installable with Bun.</p>
</header>
<ul class="grid">
${cards}
</ul>`;
  return layout(siteName, body, options);
}

export function renderAppPage(app: App, options: GenerateSiteOptions): string {
  const version = appVersion(app);
  const bins = app.surfaces.bins;
  const body = `<nav class="crumb"><a href="../../">&larr; All apps</a></nav>
<header class="site">
<h1>${escapeHtml(app.appId)}</h1>
<p>${escapeHtml(app.summary ?? app.npmName)}</p>
</header>
<div class="badges">
${version ? `<span class="badge version">v${escapeHtml(version)}</span>` : ""}
<span class="badge">${escapeHtml(app.lifecycle)}</span>
<span class="badge">${escapeHtml(app.releaseChannel)}</span>
</div>
<div class="links">
<a href="${escapeHtml(githubWebUrl(app))}">GitHub</a>
<a href="${escapeHtml(npmUrl(app))}">npm</a>
</div>
<h2>Install</h2>
<pre class="install"><code>${escapeHtml(installCommand(app))}</code></pre>
${bins.length > 0 ? `<p class="meta">Provides: ${bins.map((bin) => `<code>${escapeHtml(bin)}</code>`).join(", ")}</p>` : ""}
<p class="meta">Package: <code>${escapeHtml(app.npmName)}</code> &middot; Repo folder: <code>${escapeHtml(app.repoFolder)}</code></p>`;
  return layout(`${app.appId} — Hasna App Catalog`, body, options);
}

/**
 * Render the public catalog site (index + per-app landing pages) as static
 * HTML into `outDir`. Generation only — deploy is a separate concern.
 */
export function generateSite(options: GenerateSiteOptions): GeneratedSite {
  const apps = [...options.apps].sort((a, b) => a.appId.localeCompare(b.appId));
  const resolved = { ...options, apps };
  mkdirSync(options.outDir, { recursive: true });
  const pages: string[] = [];

  const indexPath = join(options.outDir, "index.html");
  writeFileSync(indexPath, renderIndexPage(resolved));
  pages.push(indexPath);

  for (const app of apps) {
    const appDir = join(options.outDir, "apps", app.appId);
    mkdirSync(appDir, { recursive: true });
    const pagePath = join(appDir, "index.html");
    writeFileSync(pagePath, renderAppPage(app, resolved));
    pages.push(pagePath);
  }

  return { outDir: options.outDir, pages, appCount: apps.length };
}
