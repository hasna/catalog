import { mkdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import type { App } from "./contracts.js";

export interface GenerateSiteOptions {
  apps: App[];
  outDir: string;
  siteName?: string;
  baseHref?: string;
  generatedAt?: string;
}

export interface GeneratedSite {
  outDir: string;
  pages: string[];
  appCount: number;
}

export function escapeHtml(value: string): string {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

export function appVersion(app: App): string | null {
  const version = app.metadata?.["version"];
  return typeof version === "string" && version.length > 0 ? version : null;
}

export function installCommand(app: App): string {
  return `bun add -g ${app.npmName}`;
}

function githubWebUrl(app: App): string {
  return app.githubUrl.replace(/^git\+/, "").replace(/\.git$/, "");
}

function npmUrl(app: App): string {
  return `https://www.npmjs.com/package/${app.npmName}`;
}

